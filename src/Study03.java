public class Study03 {
    /*
     + 더하기
     - 빼기
     * 곱하기
     / 나누기
     % 나눈 나머지
     */
    public static void main(String[] arg){
        int a = 3; 
        int b = 5;
        int 더하기 = (a+b);//8
        int 빼기 = (a-b);//-2
        int 곱하기 = (a * b);//15
        int 나누기 = (a / b);//0
        int 나머지 = (a % b);//3
        
        System.out.println(더하기);
        System.out.println(빼기);
        System.out.println(곱하기);
        System.out.println(나누기);
        System.out.println(나머지);

    }
    
}
